 // increment of C
#include <stdio.h>


// function main begins program execution
 int main( void )
{
	int c; //declare c as int value
	c = 5; //asing 5 to c
	printf( "%d\n",c );  //
	printf("%d\n", c++);  //postincrement
	printf("%d\n", c);
	
	
	printf("pre-increment\n");
	c = 5; //reasing 5 to c
	printf("%d\n", c);
	printf("%d\n", ++c);
	printf("%d\n", c);
	
} // end function main
